//Current frequency (double letter height)
void show_frequency(long f, int refresh, int bcolor)
{
	char *buf;
	int t1 = 0;
	int x0 = 90, y0 = 110; 
	int size = 2;
	
	if(f < 10000000)
	{
		x0 = 9;
	}	
	
	if(f == -1)
	{
		lcd_putstring(x0, y0, "       ",  3, YELLOW, bcolor);
		return;
	}
	
	if(f == 0)
	{
		lcd_putstring(x0, y0, " -------",  3, YELLOW, bcolor);
		return;
	}
		
	buf = malloc(10);
	
	//Init buffer string
	for(t1 = 0; t1 < 10; t1++)
	{
	    *(buf + t1) = 0;
	}
	
	if(f)
	{
	    int2asc(f / 100, 1, buf, 9);
	}
	else
	{
		for(t1 = 0; t1 < 8; t1++)
		{
			buf[t1] = 32;
		}
	}		
	
	//Display buffer (but only the letters that have changed)
	for(t1 = 0; *(buf + t1); t1++)
	{
		if((*(oldbuf + t1) != *(buf + t1)) || refresh)
		{
		    lcd_putchar(x0 + t1 * FONTWIDTH * size, y0, *(buf + t1), size, YELLOW, bcolor);
		}   
	}	
	
	//Copy current buffer to storing variable
	t1 = 0;
	while(*(buf + t1))
	{
		*(oldbuf + t1) = *(buf + t1);
		t1++;
	}	
	
	free(buf);
}

