//S-Meter bargraph
void smeter(int value, int bc)
{
	int v, t1, t2;
	int maxs = 200; //Max.value to be displayed in pixels
	int x = 5, y = ROW9 + 5; //Position omn screen
	static int smaxold;
	
	//Clear meter
	if(value == -1) //Fullclear
	{		
		for(t1 = 0; t1 < maxs; t1 += 4)
	    { 
		    lcd_set_xy(x + t1, y);
		    for(t2 = 0; t2 < 4; t2++)
		    {
			    lcd_draw_pixel(bc);
		    }	
		}    
		return;
	}	
	
	v = value;
	if(v > maxs)
	{
		v = maxs;
	}	
	
	if(v < smaxold)
	{
		for(t1 = v; t1 < smaxold; t1 += 4)
	    { 
		    lcd_set_xy(x + t1, y);
		    for(t2 = 0; t2 < 4; t2++)
		    {
			    lcd_draw_pixel(bc);
		    }	
		}    
		smaxold = v;
		return;
	}
	
	//Draw bar
	for(t1 = smaxold; t1 < v; t1 += 4)
	{
		lcd_set_xy(x + t1, y);
		for(t2 = 0; t2 < 4; t2++)
		{
			lcd_draw_pixel(WHITE);
		}	
	}	
	
	if(smaxold < v)
	{
		smaxold = v;
	}	
	
}

